package day05operators;

public class Increment03 {

	public static void main(String[] args) {
		
		// int data tipinde iki valiable olusturun.
		// en kısa yol ureterek 1 artırın. 
		
		//Not:En kısa yol sadece "1" artırmak icin kullanılır.
		
		int sayi1 = 12;
		sayi1++; // variable ismimden sonra iki kere "+" sembolu kullanırsak, degeri 1 artırmıs oluruz.
		System.out.println(sayi1); //13
		
		
		int sayi2 = 25;
		sayi2++;
		System.out.println(sayi2); //26

	}

}
